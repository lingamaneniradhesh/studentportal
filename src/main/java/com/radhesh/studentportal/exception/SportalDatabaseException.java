package com.radhesh.studentportal.exception;

public class SportalDatabaseException extends Exception {

	public SportalDatabaseException() {
		super();
	}

	public SportalDatabaseException(String message) {
		super(message);
	}

	public SportalDatabaseException(Throwable cause) {
		super(cause);
	}

	public SportalDatabaseException(String message, Throwable cause) {
		super(message, cause);
	}
}
